// credit: https://github.com/simias/gb-rs/blob/master/src/io/bootrom.rs

pub static BOOTROM: [u8; 0x100] = [
    // init_stack:
    0x31, 0xfe, 0xff, // ld     sp 0xfffe
    0xaf, // xor    a a
    0x21, 0xff, 0x9f, // ld     hl #vram_end
    // clear_vram:
    0x32, // ldd    [hl] a
    0xcb, 0x7c, // bit    h 7
    0x20, 0xfb, // jr nz  clear_vram
    // init_sound:
    0x21, 0x26, 0xff, // ld     hl 0xff26
    0x0e, 0x11, // ld     c 0x11
    0x3e, 0x80, // ld     a 0x80
    0x32, // ldd    [hl] a
    0xe2, // ld     [0xff00 + c] a
    0x0c, // inc    c
    0x3e, 0xf3, // ld     a 0xf3
    0xe2, // ld     [0xff00 + c] a
    0x32, // ldd    [hl] a
    0x3e, 0x77, // ld     a 0x77
    0x77, // ld     [hl] a
    // init_palette:
    0x3e, 0xfc, // ld     a 0xfc
    0xe0, 0x47, // ld     [0xff00 + #bgp] a
    // init_crc:
    0x11, 0x04, 0x01, // ld     de 0x0104
    0x21, 0x10, 0x80, // ld     hl 0x8010
    // do_crc:
    0x1a, // ld     a [de]
    0xcd, 0x95, 0x00, // call   crc_0
    0xcd, 0x96, 0x00, // call   crc_1
    0x13, // inc    de
    0x7b, // ld     a e
    0xfe, 0x34, // cp     a 0x34
    0x20, 0xf3, // jr nz  do_crc
    0x11, 0xd8, 0x00, // ld     de #tile_data
    0x06, 0x08, // ld     b 8
    // copy_tile_map:
    0x1a, // ld     a [de]
    0x13, // inc    de
    0x22, // ldi    [hl] a
    0x23, // inc    hl
    0x05, // dec    b
    0x20, 0xf9, // jr nz  copy_tile_map
    // init_tiles:
    0x3e, 0x19, // ld     a 0x19
    0xea, 0x10, 0x99, // ld     [0x9910] a
    0x21, 0x2f, 0x99, // ld     hl 0x992f
    // init_tiles_loop:
    0x0e, 0x0c, // ld     c 0x0c
    // init_tiles_inner:
    0x3d, // dec    a
    0x28, 0x08, // jr z   init_scroll
    0x32, // ldd    [hl] a
    0x0d, // dec    c
    0x20, 0xf9, // jr nz  init_tiles_inner
    0x2e, 0x0f, // ld     l 0x0f
    0x18, 0xf3, // jr     init_tiles_loop
    // init_scroll:
    0x67, // ld     h a
    // changing the following value to from 0x64 to 0x01 skips the
    // whole scrolling logo by displaying it directly in the middle of
    // the screen which makes the intro much shorter while having no
    // side effect that I know of.
    0x3e, 0x64, // ld     a 0x64
    0x57, // ld     d a
    0xe0, 0x42, // ld     [0xff00 + #scy] a
    0x3e, 0x91, // ld     a 0x91
    0xe0, 0x40, // ld     [0xff00 + #lcdc] a
    0x04, // inc    b
    // scroll_loop:
    0x1e, 0x02, // ld     e 0x02
    // wait_next_vblank:
    0x0e, 0x0c, // ld     c 0x0c
    // wait_vblank:
    0xf0, 0x44, // ld     a [0xff00 + #ly]
    0xfe, 0x90, // cp     a 0x90
    0x20, 0xfa, // jr nz  wait_vblank
    0x0d, // dec    c
    0x20, 0xf7, // jr nz  wait_vblank
    0x1d, // dec    e
    0x20, 0xf2, // jr nz  wait_next_vblank
    0x0e, 0x13, // ld     c 0x13
    0x24, // inc    h
    0x7c, // ld     a h
    0x1e, 0x83, // ld     e 0x83
    0xfe, 0x62, // cp     a 0x62
    0x28, 0x06, // jr z   play_sound
    0x1e, 0xc1, // ld     e 0xc1
    0xfe, 0x64, // cp     a 0x64
    0x20, 0x06, // jr nz  skip_sound
    // play_sound:
    0x7b, // ld     a e
    0xe2, // ld     [0xff00 + c] a
    0x0c, // inc    c
    0x3e, 0x87, // ld     a 0x87
    0xe2, // ld     [0xff00 + c] a
    // skip_sound:
    0xf0, 0x42, // ld     a [0xff00 + #scy]
    0x90, // sub    a b
    0xe0, 0x42, // ld     [0xff00 + #scy] a
    0x15, // dec    d
    0x20, 0xd2, // jr nz  scroll_loop
    0x05, // dec    b
    0x20, 0x4f, // jr nz  validate_cart
    0x16, 0x20, // ld     d 0x20
    0x18, 0xcb, // jr     scroll_loop
    // crc_0:
    0x4f, // ld     c a
    // crc_1:
    0x06, 0x04, // ld     b 0x04
    // crc_round:
    0xc5, // push   bc
    0xcb, 0x11, // rl     c
    0x17, // rl     a
    0xc1, // pop    bc
    0xcb, 0x11, // rl     c
    0x17, // rl     a
    0x05, // dec    b
    0x20, 0xf5, // jr nz  crc_round
    0x22, // ldi    [hl] a
    0x23, // inc    hl
    0x22, // ldi    [hl] a
    0x23, // inc    hl
    0xc9, // ret
    // expected_header: bytes
    0xce, 0xed, 0x66, 0x66, 0xcc, 0x0d, 0x00, 0x0b, 0x03, 0x73, 0x00, 0x83, 0x00, 0x0c, 0x00, 0x0d,
    0x00, 0x08, 0x11, 0x1f, 0x88, 0x89, 0x00, 0x0e, 0xdc, 0xcc, 0x6e, 0xe6, 0xdd, 0xdd, 0xd9, 0x99,
    0xbb, 0xbb, 0x67, 0x63, 0x6e, 0x0e, 0xec, 0xcc, 0xdd, 0xdc, 0x99, 0x9f, 0xbb, 0xb9, 0x33, 0x3e,
    // tile_data: bytes
    0x3c, 0x42, 0xb9, 0xa5, 0xb9, 0xa5, 0x42, 0x3c, // validate_cart:
    0x21, 0x04, 0x01, // ld     hl, 0x0104
    0x11, 0xa8, 0x00, // ld     de, #expected_header
    // check_header:
    0x1a, // ld     a [de]
    0x13, // inc    de
    0xbe, // cp     a [hl]
    // this is an infinite loop when the checksum fails. Replacing
    // it with 0x00 0x00 (nop nop) will allow invalid roms to run.
    0x20, 0xfe, // jr nz  .
    0x23, // inc    hl
    0x7d, // ld     a l
    0xfe, 0x34, // cp     a 0x32
    0x20, 0xf5, // jr nz  check_header
    0x06, 0x19, // ld     b 0x19
    0x78, // ld     a b
    // header_sum
    0x86, // add    a [hl]
    0x23, // inc    hl
    0x05, // dec    b
    0x20, 0xfb, // jr nz  header_sum
    0x86, // add    a [hl]
    // same as above, infinite loop if the sum is bad, replace with
    // nops to run anyway.
    0x20, 0xfe, // jr nz  .
    0x3e, 0x01, // ld a   0x1
    // there shouldn't be anything at that address, I assume that's
    // how you tell the hardware to unmap the bootrom.
    0xe0, 0x50, // ld [0xff00 + 0x50] a
];
